# Toshkent shahrining tumanlar uchun: 4-harfli kod → tuman nomi
# AVIA_CODES ichidan ST bilan boshlanadigan yozuvlardan avtomatik quriladi
TASHKENT_DISTRICT_CODE_TO_NAME: dict[str, str] = {
    code: name.replace("_", " ").title()
    for name, code in AVIA_CODES.items()
    if code.startswith("ST")
}


def decode_region_key(region_key: str) -> str:
    """
    Hudud kalitini o'qilishi qulay nomga aylantiradi.

    Toshkent shahri uchun 4-harfli kod (masalan STCH) tumaniga (Chilonzor),
    boshqa hududlar uchun 2-harfli prefiks (masalan SS) viloyat nomiga (Samarqand viloyati)
    aylantiriladi.
    """
    upper_key = region_key.upper()
    if upper_key.startswith("ST") and len(upper_key) == 4:
        return TASHKENT_DISTRICT_CODE_TO_NAME.get(upper_key, f"Toshkent ({upper_key})")
    prefix = upper_key[:2]
    return REGION_PREFIX_TO_NAME.get(prefix, region_key)